<script>
// eslint-disable-next-line object-curly-newline
import { mdiFacebook, mdiTwitter, mdiGithub, mdiGoogle, mdiEyeOutline, mdiEyeOffOutline } from '@mdi/js'
import { Axios } from '@/api/axios'

export default {
  data() {
    return {
      username: '',
      password: '',
      loginData: '',
      isPasswordVisible: false,
      error: '',
    }
  },
  methods: {
    async login() {
      localStorage.clear()
      try {
        this.loginData = await Axios.post(`${this.$route.name.replace('-login', '/')}auth/login`, {
          username: this.username,
          password: this.password,
        })
        if (this.loginData.data.status) {
          localStorage.setItem('token', this.loginData.data.data.token)
          localStorage.setItem('refreshToken', this.loginData.data.data.refreshToken)
          localStorage.setItem('authenticated', true)
          localStorage.setItem('role', this.$route.name.replace('-login', ''))
          window.location.href = '/'
        } else {
          this.$store.commit('setNotification', {
            title: this.loginData.data.message,
            type: 'error',
            isShow: true,
          })
          this.error = this.loginData.data.message
        }
      } catch (error) {
        this.$store.commit('setNotification', {
          title: 'Logged in not successfully',
          type: 'error',
          isShow: true,
        })
        console.log(error)
      }
    },
  },
  setup() {
    const socialLink = [
      {
        icon: mdiFacebook,
        color: '#4267b2',
        colorInDark: '#4267b2',
      },
      {
        icon: mdiTwitter,
        color: '#1da1f2',
        colorInDark: '#1da1f2',
      },
      {
        icon: mdiGithub,
        color: '#272727',
        colorInDark: '#fff',
      },
      {
        icon: mdiGoogle,
        color: '#db4437',
        colorInDark: '#db4437',
      },
    ]

    return {
      socialLink,
      icons: {
        mdiEyeOutline,
        mdiEyeOffOutline,
      },
    }
  },
}
</script>

